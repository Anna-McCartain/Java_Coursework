public class DuplicateVotersException extends Exception
{
  public DuplicateVotersException()
  {
    super();
  }

  public DuplicateVotersException(String message)
  {
    super(message);
  }

  public DuplicateVotersException(String message, Throwable cause)
  {
    super(message, cause);
  }

  public DuplicateVotersException(Throwable cause)
  {
    super(cause);
  }
}

