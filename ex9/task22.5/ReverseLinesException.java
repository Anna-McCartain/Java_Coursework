public class ReverseLinesException extends Exception
{
  public ReverseLinesException()
  {
    super();
  }

  public ReverseLinesException(String message)
  {
    super(message);
  }

  public ReverseLinesException(String message, Throwable cause)
  {
    super(message, cause);
  }

  public ReverseLinesException(Throwable cause)
  {
    super(cause);
  }
}

